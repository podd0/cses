using T = pii;
int set_range_count(ordered_set<T> &st, T a, T b)
{
	if(st.empty() || b < a) return 0;
	auto ita = st.lower_bound(a);
	auto itb = st.upper_bound(b);
	int ord_a = (ita == st.end()? ssize(st): int(st.order_of_key(*ita)));
	int ord_b = (itb == st.end()? ssize(st): int(st.order_of_key(*itb)));
	return ord_b-ord_a;
}
